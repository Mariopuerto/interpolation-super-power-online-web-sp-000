colors = ["red", "orange", "yellow", "green", "blue", "indigo", "violet"]

def display_rainbow(rainbow)
 puts  "R: #{rainbow[0]}"
 puts  "O: #{rainbow[1]}"
 puts  "Y: #{rainbow[2]}"
 puts  "G: #{rainbow[3]}"
 puts  "B: #{rainbow[4]}"
 puts  "I: #{rainbow[5]}"
 puts  "V: #{rainbow[6]}"
end

display_rainbow(colors)